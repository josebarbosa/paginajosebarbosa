<h1>Sobre o simulador de salários do MPU e Judiciário</h1>
<h3>Sobre o Sistema</h3>
<p>Esta página foi criada por José Antonio dos Santos Barbosa, servidor do MPU. Trata-se de um simulador que visa tornar mais fácil para servidores destas instituições, e demais interessados, o cálculo da real remuneação dos
servidores, de acordo com uma série de parâmetros de sua vida cotidiana, como acumulos de gratificações, retribuição por qualificação ou exercício de funções ou cargos em comissão. </p>
<p>Alguns valores são apenas aproximados, ou simplificados em relação ao que são exibidos no contra-cheque, de forma que podem apresentar pequenas diferenças. </p>
<p>Outro ponto importante é que naõ estão contempladas TODAS as situações possíveis para um servidor, mas sim atender adequadamente a pelo menos a maioria absoluta delas. Eventuais casos específicos podem não ser contemplados
por este simulador. </p>
<h3>Como simular</h3>
<p>Preencha o formulário atentamente, de acordo com as informações prestadas, e clique no botão calcular. Através destes parâmetros, o sistema calculará o salário baseado nos projetos de lei aprovados na Câmara (MPU:
substitutivo ao PL 6697/2009 e Judiciário 2648/2015). As informações são disponibilizadas de maneira agrupada por rubrica, o que pode ser diferente do que algumas instituições adotam, como, por exemplo, no caso
dos adicionais de treinamento. </p>
<p>É importante observar que caso queira incluir alguma mudança ao longo de um período, é preciso fazer uma nova simulação, com a alteração de parâmetros. O sistema atualmente só é capaz de incluir a progressão funcional, se
marcada. </p>
<p><b>Dica:</b> utilize o botão VOLTAR para não perder os dados armazendos na simulação. </p>
